package MyDay;

public class Customer extends BusReservation {
	String FN;
	String LN;
	String DOB;
	String Gender;
	String Educate;

	public String getFN() {
		return FN;
	}

	public void setFN(String fN) {
		FN = fN;
	}

	public String getLN() {
		return LN;
	}

	public void setLN(String lN) {
		LN = lN;
	}

	public String getDOB() {
		return DOB;
	}

	public void setDOB(String dOB) {
		DOB = dOB;
	}

	public String getGender() {
		return Gender;
	}

	public void setGender(String gender) {
		Gender = gender;
	}

	public String getEducate() {
		return Educate;
	}

	public void setEducate(String educate) {
		Educate = educate;
	}

}
